def get_sigungu_coord(cortar_no: str) -> tuple[float, float] | None:
    """cortarNo에서 시군구 중심 좌표 반환"""
    key = cortar_no[:7]
    if key in SIGUNGU_COORDS:
        return SIGUNGU_COORDS[key]
    # 앞 5자리로도 시도
    for k, v in SIGUNGU_COORDS.items():
        if k[:5] == cortar_no[:5]:
            return v
    return None


def get_sido_list() -> list[dict]:
    """시도 목록 반환"""
    return [{"cortarNo": code, "cortarNm": name} for name, code in SIDO.items()]


def get_sigungu_list(sido_code: str) -> list[dict]:
    """시군구 목록 반환"""
    sigungu = SIGUNGU.get(sido_code, {})
    return [{"cortarNo": code, "cortarNm": name} for name, code in sigungu.items()]
